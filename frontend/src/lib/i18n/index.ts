export function t(key: string, locale: string = 'ro'): string {
  return translations[locale]?.[key] || key;
}

// Helper for property type labels
export function getPropertyTypeLabel(value: string): string {
  return t(`property_type.${value}`);
}

// Helper for room labels
export function getRoomLabel(value: string): string {
  return t(`room.${value}`);
}

// Helper for amenity labels
export function getAmenityLabel(value: string): string {
  return t(`amenity.${value}`);
}

// Helper for field labels (translates English backend labels to Romanian)
export function getFieldLabel(label: string, fieldId?: string): string {
  // First try to translate using the label directly
  const translatedLabel = t(`label.${label}`);
  if (translatedLabel !== `label.${label}`) {
    return translatedLabel;
  }
  
  // Fallback to field ID if provided
  if (fieldId) {
    const fieldTranslation = t(`field.${fieldId}`);
    if (fieldTranslation !== `field.${fieldId}`) {
      return fieldTranslation;
    }
  }
  
  // Return original if no translation found
  return label;
}

// Helper for option labels (translates dropdown option labels)
export function getOptionLabel(label: string): string {
  const translatedLabel = t(`option.${label}`);
  if (translatedLabel !== `option.${label}`) {
    return translatedLabel;
  }
  return label;
}

// Helper to translate options array
export function translateOptions(options?: Array<{value: string, label: string}>): Array<{value: string, label: string}> | undefined {
  if (!options) return options;
  return options.map(opt => ({
    value: opt.value,
    label: getOptionLabel(opt.label)
  }));
}